package chapter3Exercises;

public class PythagorasTripes {

    public static void main(String[] args) {

        for (int sideA =1; sideA<=10; sideA ++){
            for (int sideB =1; sideB <= 10; sideB ++){
                for (int sideC =1; sideC <= 10; sideC++){
                    System.out.println(sideA + " " + sideB + " " + sideC);
                }
            }


        }

    }
}
